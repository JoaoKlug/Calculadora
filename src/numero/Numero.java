package numero;

public class Numero {
	
	private int id;
	private int numero;
	private boolean primo;
	private long fibonacci;
	private double raiz;
	private long fatorial;
	private long binario;
	private String hexadecimal;

	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}

	public boolean isPrimo() {
		return primo;
	}
	public void setPrimo(boolean primo) {
		this.primo = primo;
	}
	
	public long getFibonacci() {
		return fibonacci;
	}
	public void setFibonacci(long fibonacci) {
		this.fibonacci = fibonacci;
	}
	
	public double getRaiz() {
		return raiz;
	}
	public void setRaiz(double raiz) {
		this.raiz = raiz;
	}
	
	public long getFatorial() {
		return fatorial;
	}
	public void setFatorial(long fatorial) {
		this.fatorial = fatorial;
	}
	
	public long getBinario() {
		return binario;
	}
	public void setBinario(long binario) {
		this.binario = binario;
	}
	
	public String getHexadecimal() {
		return hexadecimal;
	}
	public void setHexadecimal(String hexadecimal) {
		this.hexadecimal = hexadecimal;
	}
	
}
	
